class WEAPON():
    def __init__(self,name,type,damage):
        self.name = name
        self.type = type
        self.damage = damage
class POTION():
    def __init__(self,name,type,effect):
        self.name = name
        self.type = type
        self.effect = effect
class ARMOUR():
    def __init__(self, name, defense):
        self.name = name
        self.defense = defense